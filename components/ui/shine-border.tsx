'use client'

import React from 'react'
import { ny } from '~/lib/utils'

type TColorProp = `#${string}` | `#${string}`[]
interface ShineBorderProps {
   borderRadius?: number
   borderWidth?: number
   duration?: number
   color?: TColorProp
   className?: string
   children: React.ReactNode
}

export function ShineBorder({
   borderRadius = 8,
   borderWidth = 1,
   duration = 14,
   color = '#fff',
   className,
   children,
}: ShineBorderProps) {
   return (
      <div
         style={
            {
               '--border-radius': `${borderRadius}px`,
            } as React.CSSProperties
         }
         className={ny(
            'relative grid min-h-[60px] w-fit min-w-[300px] place-items-center rounded-[--border-radius] bg-black p-3 text-white',
            className,
         )}
      >
         <div
            style={
               {
                  '--border-width': `${borderWidth}px`,
                  '--border-radius': `${borderRadius}px`,
                  '--border-radius-child': `${borderRadius * 0.2}px`,
                  '--shine-pulse-duration': `${duration}s`,
                  '--mask-linear-gradient': `linear-gradient(#fff 0 0) content-box, linear-gradient(#fff 0 0)`,
                  '--background-radial-gradient': `radial-gradient(transparent,transparent, ${
              !Array.isArray(color) ? color : color.join(',')
            },transparent,transparent)`,
               } as React.CSSProperties
            }
            className={`before:bg-shine-size before:absolute before:inset-0 before:aspect-square before:size-full before:rounded-[--border-radius] before:p-[--border-width] before:will-change-[background-position] before:content-[""] before:![-webkit-mask-composite:xor] before:![mask-composite:exclude] before:[background-image:var(--background-radial-gradient)] before:[background-size:300%_300%] before:[mask:var(--mask-linear-gradient)] motion-safe:before:animate-[shine-pulse_var(--shine-pulse-duration)_infinite_linear]`}
         >
         </div>
         <div className="z-[1] size-full rounded-[--border-radius-child]">
            {children}
         </div>
      </div>
   )
}
